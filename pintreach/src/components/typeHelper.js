export default function typeHelper(type){
	if (type === 'medicine') {
		return 'https://image.flaticon.com/icons/svg/1546/1546076.svg';
	} else if (type === 'physics') {
		return 'https://image.flaticon.com/icons/svg/124/124675.svg';
	} else if (type === 'biology') {
		return 'https://image.flaticon.com/icons/svg/1548/1548234.svg';
	} else if (type === 'economics') {
		return 'https://image.flaticon.com/icons/svg/1042/1042627.svg';
	} else if (type === 'chemistry') {
		return 'https://image.flaticon.com/icons/svg/112/112731.svg';
	} else if (type === 'environmental science') {
		return 'https://image.flaticon.com/icons/svg/1598/1598238.svg';
	} else if (type === 'computer science') {
		return 'https://image.flaticon.com/icons/svg/1197/1197460.svg';
	} else return null;
};

{
/* <option defaultValue="medicine">medicine</option>
<option value="physics">physics</option>
<option value="biology">biology</option>
<option value="economics">economics</option>
<option value="chemistry">chemistry</option>
<option value="environmental science">environmental science</option>
<option value="computer science">computer science</option> */
}
